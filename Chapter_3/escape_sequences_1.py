# Escape sequences characters
# Examples of common escape sequences in Python:
# \n = newline
# \t = tab
# \\ = backslash
# \" = double quote
# \\' = single quote
print("Hello\nWorld")        # \n se newline insert hota hai
print("List:\tApple, Banana, Cherry")  # \t se tab insert hota hai
print("Path: C:\\Users\\David")  # \\ se backslash print hota hai
print("She said, \"Hello!\"")  # \" se double quote string me include hota hai
print('It\'s easy to use escape sequences.')  # \' se single quote string me include hota hai
