# what is an arithmetic operator?
# an arithmetic operator is a symbol that performs a mathematical operation on two operands.
# the following are the arithmetic operators in python:
# + (addition)
# - (subtraction)
# * (multiplication)
# / (division)
# = assignment operator


# let's see how to use these operators in python:

# assignment operator
x = 10
y = 5
a = 20
a += 5  # equivalent to a = a + 5
b = 10
b -= 3  # equivalent to b = b - 3
c = 10
c /= 2  # equivalent to c = c / 2
d = 4
d *= 3  # equivalent to d = d * 3

# comparison operator
op = 5<10  # less than
op1 = 5>10  # greater than
op2 = 5<=10  # less than or equal to
op3 = 5>=10  # greater than or equal to
op4 = 5==10  # equal to
op5 = 5!=10  # not equal to
fg = 5 == 5  # equal to
fg = 5 != 10  # not equal to

# logical operator
X = True or False  # logical OR
Y = True and False  # logical AND
# truth table for logical OR
# A     B     A or B
# True  True  True
# True  False True
# False True  True
# False False False

# truth table for logical AND
# A     B     A and B
# True  True  True
# True  False False
# False True  False
# False False False

# logical NOT
Z = not True  # logical NOT
W = not False  # logical NOT
print(not(True))  # False
print(not(False))  # True


