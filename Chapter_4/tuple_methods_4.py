a = (1, 2, 3, 4, 5)
print(type(a))  # Initialize a tuple with integers

# Tuple methods demonstration
print("Length of tuple:", len(a))
print("Count of 3 in tuple:", a.count(3))
print("Index of 4 in tuple:", a.index(4))

# Other tuple operations
print("Maximum value:", max(a))
print("Minimum value:", min(a))
print("Sum of elements:", sum(a))